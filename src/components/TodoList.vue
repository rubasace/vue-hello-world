<template>
    <div class="todo-list">
        <h1>{{ title }}</h1>
        <p>
            <input
                    type="text"
                    class="input"
                    v-model="filter"
            >
        </p>
        <ol v-show="filteredItems.length>0">
            <li v-for="(item,index) in filteredItems" :key="index">{{item}}
                <button @click="deleteElement(index)">X</button>
            </li>
        </ol>
        <p v-show="items.length===0">No tasks yet :(</p>
        <p v-show="items.length>0 && filteredItems.length === 0">No tasks meet your criteria</p>
        <p>
            <input
                    type="text"
                    class="input"
                    @keypress.enter="addElement"
                    v-model="newElement"
            >
        </p>
        <p>
            <button @click="clear">Clear {{items.length}}</button>
        </p>
    </div>
</template>

<script>
    import {Component, Prop, Vue} from "vue-property-decorator";

    export default {
        name: "TodoList",
        components: {},
        props: {
            title: {
                type: String,
                default: "TO-DO List"
            }
        },
        data() {
            return {
                items: [],
                newElement: "",
                filter: ""
            }
        },
        computed: {
            filteredItems() {
                return !this.filter ? this.items : this.items.filter(e => e.includes(this.filter))
            }
        },
        methods: {
            addElement() {
                this.items.push(this.newElement)
                this.newElement = ""
            },
            clear() {
                this.items = []
            },
            deleteElement(index) {
                this.items.splice(index, 1)
            }
        }
    };

    // @Component
    // export default class HelloWorld extends Vue {
    //   @Prop() private msg!: string;
    // }
</script>

